package multipleBlock;

public class Pick{
	
	private double x;
	private double y;
	
	private int block;
	private int aisle;	
	private int locate;
	private int side;
	
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	public int getBlock() {
		return block;
	}
	public void setBlock(int block) {
		this.block = block;
	}
	public int getAisle() {
		return aisle;
	}
	public void setAisle(int aisle) {
		this.aisle = aisle;
	}
	public int getLocate() {
		return locate;
	}
	public void setLocate(int locate) {
		this.locate = locate;
	}
	public int getSide() {
		return side;
	}
	public void setSide(int side) {
		this.side = side;
	}
	
}
